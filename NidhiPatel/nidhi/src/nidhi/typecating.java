package nidhi;

public class typecating {

	public static void main(String[] args) {
		short s=4;
		int x=s;
		System.out.println(x);
		int y=7;
		short z=(short)y;
		System.out.println(z);
		double d=78.77;
		float f=(float)d;
		System.out.println(f);
		double dd=f;
		System.out.println(dd);
		long l=(long)f;
		System.out.println(l);
	}

}
