package nidhi;

public class airthmetic_operator {

	public static void main(String[] args) {
		int x=50;
		int y=50;
		//post increment
		System.out.println(x++);
		System.out.println(x);
		//post decrement
		System.out.println(x--);
		System.out.println(x);
		//pre increment
		System.out.println(++x);
		System.out.println(x);
		//pre decrement
		System.out.println(--x);
		System.out.println(x);
		//addition
		System.out.println(x+y);
		//subtraction
		System.out.println(x-y);
		//multiplication
		System.out.println(x*y);
		//division
		System.out.println(x/y);
		//mod
		System.out.println(x%y);
		
		;
		

	}

}
